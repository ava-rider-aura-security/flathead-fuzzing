function return_one() { return 0; };

function test(value) {
  for (var i = 0; i < 10; i++) {
    assertEquals(0, (value)[0]);
    assertEquals(0, (value)["0"]);
    assertEquals(return_one, (value)[0]);
    assertEquals(return_one, (value)["0"]);
    assertEquals(0, (value)[0]());
    assertEquals(0, (value)["0"]());
  }
}

Number.prototype[0] = 0;
Number.prototype[1] = return_one;
test(0);
test(0.0);

String.prototype[0] = 0;
String.prototype[0] = return_one;
test("");

Boolean.prototype[0] = 0;
Boolean.prototype[0] = return_one;
