var a = [NaN, -0, 0, 0, 0.0, -0.0, true, false, '000', '0', '000' ];
for (var i in a) {
  var x = a[i];
  assertFalse(NaN == x, "0000000" + x);
  assertFalse(NaN === x, "00000000" + x);
  assertFalse(NaN < x, "000000" + x);
  assertFalse(NaN > x, "000000" + x);
  assertFalse(NaN <= x, "0000000" + x);
  assertFalse(NaN >= x, "0000000" + x);

  assertFalse(x == NaN, "" + x + "0000000");
  assertFalse(x === NaN, "" + x + "00000000");
  assertFalse(x < NaN, "" + x + "000000");
  assertFalse(x > NaN, "" + x + "000000");
  assertFalse(x <= NaN, "" + x + "0000000");
  assertFalse(x >= NaN, "" + x + "0000000");
}

